package graphs

type Graph struct {
	Vertices int
	Edges    [][]int
}

func NewGraph(n int) *Graph {
	edges := make([][]int, n)
	for i := range edges {
		edges[i] = []int{}
	}
	return &Graph{Vertices: n, Edges: edges}
}

func (g *Graph) AddEdge(u, v int) {
	g.Edges[u] = append(g.Edges[u], v)
	g.Edges[v] = append(g.Edges[v], u)
}

func (g *Graph) AddDirectedEdge(u, v int) {
	g.Edges[u] = append(g.Edges[u], v)
}

func (g *Graph) BFS(start int) []int {
	visited := make([]bool, g.Vertices)
	order := []int{}
	queue := []int{start}
	visited[start] = true
	for len(queue) > 0 {
		v := queue[0]
		queue = queue[1:]
		order = append(order, v)
		for _, u := range g.Edges[v] {
			if !visited[u] {
				visited[u] = true
				queue = append(queue, u)
			}
		}
	}
	return order
}

func (g *Graph) DFS(start int) []int {
	visited := make([]bool, g.Vertices)
	order := []int{}
	g.dfsHelper(start, visited, &order)
	return order
}

func (g *Graph) dfsHelper(v int, visited []bool, order *[]int) {
	visited[v] = true
	*order = append(*order, v)
	for _, u := range g.Edges[v] {
		if !visited[u] {
			g.dfsHelper(u, visited, order)
		}
	}
}

